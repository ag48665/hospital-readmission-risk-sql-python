import pandas as pd

data = {
    "age": [72, 64, 45, 58, 29, 81, 67, 52],
    "bmi": [31.2, 28.5, 24.1, 33.8, 22.4, 30.6, 27.9, 35.1],
    "diabetes": [1, 0, 0, 1, 0, 1, 0, 1],
    "hypertension": [1, 1, 0, 1, 0, 1, 1, 0],
    "chronic_disease": [1, 1, 0, 1, 0, 1, 1, 1],
    "length_of_stay": [7, 5, 2, 9, 2, 12, 5, 7],
    "icu_admission": [1, 0, 0, 1, 0, 1, 0, 1],
    "total_cost": [12500, 7800, 3200, 15200, 2100, 23000, 6900, 16800],
    "readmitted_30_days": [1, 1, 0, 1, 0, 1, 0, 1]
}

df = pd.DataFrame(data)

df.head()

df.info()
df.describe()

readmission_rate = df["readmitted_30_days"].mean() * 100
print(f"30-day readmission rate: {readmission_rate:.2f}%")

from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, classification_report

# Features
X = df.drop("readmitted_30_days", axis=1)

# Target
y = df["readmitted_30_days"]

# Train/test split
X_train, X_test, y_train, y_test = train_test_split(
    X,
    y,
    test_size=0.25,
    random_state=42
)

# Model
model = LogisticRegression()

# Train
model.fit(X_train, y_train)

# Predict
y_pred = model.predict(X_test)

# Accuracy
accuracy = accuracy_score(y_test, y_pred)

print(f"Model Accuracy: {accuracy:.2f}")

# Classification report
print(classification_report(y_test, y_pred))

feature_importance = pd.DataFrame({
    "Feature": X.columns,
    "Coefficient": model.coef_[0]
})

feature_importance = feature_importance.sort_values(
    by="Coefficient",
    ascending=False
)

print(feature_importance)

from sklearn.metrics import roc_curve, roc_auc_score
import matplotlib.pyplot as plt

# Probability predictions
y_probs = model.predict_proba(X_test)[:, 1]

# ROC metrics
fpr, tpr, thresholds = roc_curve(y_test, y_probs)

# AUC
auc_score = roc_auc_score(y_test, y_probs)

# Plot
plt.figure(figsize=(6,4))
plt.plot(fpr, tpr, label=f"AUC = {auc_score:.2f}")
plt.plot([0,1], [0,1], linestyle='--')

plt.xlabel("False Positive Rate")
plt.ylabel("True Positive Rate")
plt.title("ROC Curve - Hospital Readmission Prediction")
plt.legend()

plt.show()

